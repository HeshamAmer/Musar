package com.musar.Database;
/**
 * 
 * id of banned application
 * the name of banned application
 *
 */
public class BannedApplication {

	int id;
	String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
